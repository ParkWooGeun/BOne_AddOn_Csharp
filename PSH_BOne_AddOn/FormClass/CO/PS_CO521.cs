        /// <summary>
        /// VALIDATE 이벤트
        /// </summary>
        /// <param name="FormUID">Form UID</param>
        /// <param name="pVal">ItemEvent 객체</param>
        /// <param name="BubbleEvent">BubbleEvnet(true, false)</param>
        private void Raise_EVENT_VALIDATE(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
        {
            string sQry;
            SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

            try
            {
                oForm.Freeze(true);

                if (pVal.Before_Action == true)
                {
                    if (pVal.ItemChanged == true)
                    {
                        if (pVal.ItemUID == "Mat")
                        {
                            if (pVal.ColUID == "PP030No") //작지문서번호
                            {
                                oMat.FlushToDataSource();

                                sQry = "EXEC [PS_CO521_01] '" + oDS_PS_CO521L.GetValue("U_PP030No", pVal.Row - 1) + "'";
                                oRecordSet.DoQuery(sQry);

                                oDS_PS_CO521L.SetValue("U_OrdNum", pVal.Row - 1, oRecordSet.Fields.Item("OrdNum").Value); //작번
                                oDS_PS_CO521L.SetValue("U_ItemName", pVal.Row - 1, oRecordSet.Fields.Item("ItemName").Value); //품명
                                oDS_PS_CO521L.SetValue("U_ItemSpec", pVal.Row - 1, oRecordSet.Fields.Item("ItemSpec").Value); //규격
                                oDS_PS_CO521L.SetValue("U_ORDRAmt", pVal.Row - 1, oRecordSet.Fields.Item("ORDRAmt").Value); //수주금액
                                
                                if (oMat.RowCount == pVal.Row && !string.IsNullOrEmpty(oDS_PS_CO521L.GetValue("U_" + pVal.ColUID, pVal.Row - 1).ToString().Trim()))
                                {
                                    PS_CO521_AddMatrixRow(pVal.Row, false);
                                }

                                oMat.LoadFromDataSource();
                            }

