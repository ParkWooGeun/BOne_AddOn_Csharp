        /// <summary>
        /// 콤보박스에 기본값설정
        /// </summary>
        private void PS_CO665_ComboBox_Setting()
        {
            string sQry;
            PSH_DataHelpClass dataHelpClass = new PSH_DataHelpClass();

            try
            {
                oForm.Freeze(true);

                //사업장
                sQry = "  SELECT    CONVERT(VARCHAR(1),BPLID) AS [BPLID],";
                sQry += "           BPLName AS [BPLName]";
                sQry += " FROM      OBPL";
                sQry += " UNION ALL";
                sQry += " SELECT   '%' AS [BPLID],";
