		/// <summary>
		/// Raise_EVENT_ITEM_PRESSED
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_ITEM_PRESSED(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			string errMessage = string.Empty;

			try
			{
				if (pVal.BeforeAction == true)
				{
					if (pVal.ItemUID == "48")
					{
						if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
						{
						}
						else if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_UPDATE_MODE)
						{
							if (Convert.ToDouble(oMat01.Columns.Item("55").Cells.Item(oMatTopRow01).Specific.Value.ToString().Trim()) >= Convert.ToDouble(oMat02.Columns.Item("4").Cells.Item(oMatBottomLeftRow01).Specific.Value.ToString().Trim()))
							{
							}
							else
							{
								errMessage = "선택수량이 필요총계를 초과하였습니다.";
								throw new Exception();
							}
						}
						else if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_OK_MODE)
						{
							if (Convert.ToDouble(oMat01.Columns.Item("55").Cells.Item(oMatTopRow01).Specific.Value.ToString().Trim()) >= Convert.ToDouble(oMat02.Columns.Item("4").Cells.Item(oMatBottomLeftRow01).Specific.Value.ToString().Trim()))
							{
							}
							else
							{
								errMessage = "선택수량이 필요총계를 초과하였습니다.";
								throw new Exception();
							}
						}
					}
				}
				else if (pVal.BeforeAction == false)
				{
				}
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
				BubbleEvent = false;
			}
		}

