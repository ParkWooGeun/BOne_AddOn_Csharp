		/// <summary>
		/// PS_GA501_MTX01
		/// </summary>
		private void PS_GA501_MTX01()
		{
			string sQry;
			string StdYear;	 //기준년도
			string ItemType; //품목타입
			string errMessage = string.Empty;
			SAPbouiCOM.ProgressBar ProgressBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);

			try
			{
				oForm.Freeze(true);
				StdYear = oForm.Items.Item("StdYear").Specific.Value.ToString().Trim();
				ItemType = oForm.Items.Item("ItemType").Specific.Value.ToString().Trim();

				ProgressBar01.Text = "조회 시작....";

				sQry = " EXEC PS_GA501_01 '";
				sQry += StdYear + "','";
				sQry += ItemType + "'";

				oGrid.DataTable.Clear();
				oDS_PS_GA501A.ExecuteQuery(sQry);

				oGrid.Columns.Item(2).RightJustified = true;
				oGrid.Columns.Item(3).RightJustified = true;
				oGrid.Columns.Item(4).RightJustified = true;

				if (oGrid.Rows.Count == 0)
				{
					errMessage = "결과가 존재하지 않습니다.";
					throw new Exception();
				}

				oGrid.AutoResizeColumns();
				oForm.Update();
			}
			catch (Exception ex)
			{
				if (ProgressBar01 != null)
				{
					ProgressBar01.Stop();
				}
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
			}
			finally
			{
				ProgressBar01.Stop();
				System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgressBar01);
				oForm.Freeze(false);
			}
		}

