		/// <summary>
		/// PS_MM003_MatrixSpaceLineDel
		/// </summary>
		/// <returns></returns>
		private bool PS_MM003_MatrixSpaceLineDel()
		{
			bool functionReturnValue = false;
			int i;
			string errMessage = string.Empty;

			try
			{
				oMat.FlushToDataSource();
				
				if (oMat.VisualRowCount <= 1) // 라인
				{
					errMessage = "라인 데이터가 없습니다. 확인하세요.";
					throw new Exception();
				}

				if (oMat.VisualRowCount > 0)
				{
					for (i = 0; i <= oMat.VisualRowCount - 2; i++)
					{
						oDS_PS_MM003L.Offset = i;

						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_TrDiv", 0).ToString().Trim()))
						{
							errMessage = "구분은 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_TrDivNm", 0).ToString().Trim()))
						{
							errMessage = "장/단거리명은 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_LocCode", 0).ToString().Trim()))
						{
							errMessage = "지역코드는 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_LocName", 0).ToString().Trim()))
						{
							errMessage = "지역명은 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_Tonnage", 0).ToString().Trim()))
						{
							errMessage = "톤수구분은 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_TonNm", 0).ToString().Trim()))
						{
							errMessage = "톤수명은 필수입니다. 확인하세요.";
							throw new Exception();
						}
						if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_Amt", 0).ToString().Trim()))
						{
							errMessage = "금액은 필수입니다. 확인하세요.";
							throw new Exception();
						}
					}

					if (string.IsNullOrEmpty(oDS_PS_MM003L.GetValue("U_TrDiv", oMat.VisualRowCount - 1).ToString().Trim()))
					{
						oDS_PS_MM003L.RemoveRecord(oMat.VisualRowCount - 1);
					}
				}

				oMat.LoadFromDataSource();
				functionReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
			}
			return functionReturnValue;
		}

