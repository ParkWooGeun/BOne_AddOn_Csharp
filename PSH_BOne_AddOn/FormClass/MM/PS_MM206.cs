		/// <summary>
		/// PS_MM206_ComboBox_Setting
		/// </summary>
		private void PS_MM206_ComboBox_Setting()
		{
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				sQry = "SELECT BPLId, BPLName From OBPL Order by BPLId";
				oRecordSet.DoQuery(sQry);
				while (!oRecordSet.EoF)
				{
					oForm.Items.Item("BPLId").Specific.ValidValues.Add(oRecordSet.Fields.Item(0).Value.ToString().Trim(), oRecordSet.Fields.Item(1).Value.ToString().Trim());
					oRecordSet.MoveNext();
				}
				oForm.Items.Item("BPLId").Specific.Select(0, SAPbouiCOM.BoSearchKey.psk_Index);

				// 아이템 대분류
				sQry = "SELECT Code, Name From [@PSH_ITMBSORT] Order by Code";
				oRecordSet.DoQuery(sQry);
				oForm.Items.Item("ItmBsort").Specific.ValidValues.Add("%", "전체");
				while (!oRecordSet.EoF)
				{
					oForm.Items.Item("ItmBsort").Specific.ValidValues.Add(oRecordSet.Fields.Item(0).Value.ToString().Trim(), oRecordSet.Fields.Item(1).Value.ToString().Trim());
					oRecordSet.MoveNext();
				}
				oForm.Items.Item("ItmBsort").Specific.Select(0, SAPbouiCOM.BoSearchKey.psk_Index);

				// 아이템 중분류
				sQry = "SELECT U_Code,U_CodeName FROM [@PSH_ITMMSORT] Order by U_Code";
				oRecordSet.DoQuery(sQry);
				if (oForm.Items.Item("ItmMsort").Specific.ValidValues.Count == 0)
				{
					oForm.Items.Item("ItmMsort").Specific.ValidValues.Add("%", "전체");
				}
				while (!oRecordSet.EoF)
				{
					oForm.Items.Item("ItmMsort").Specific.ValidValues.Add(oRecordSet.Fields.Item(0).Value.ToString().Trim(), oRecordSet.Fields.Item(1).Value.ToString().Trim());
					oRecordSet.MoveNext();
				}
				oForm.Items.Item("ItmMsort").Specific.Select(0, SAPbouiCOM.BoSearchKey.psk_Index);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet); //메모리 해제
			}
		}

		/// <summary>
		/// PS_MM206_HeaderSpaceLineDel
		/// </summary>
		/// <returns></returns>
		private bool PS_MM206_HeaderSpaceLineDel()
		{
			bool ReturnValue = false;
			string errMessage = string.Empty;

			try
			{
				if (string.IsNullOrEmpty(oForm.Items.Item("YYYY").Specific.Value.ToString().Trim()))
				{
					errMessage = "년도는 필수사항입니다. 확인하여 주십시오.";
					throw new Exception();
				}
				ReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
			}
			return ReturnValue;
		}

		/// <summary>
		/// PS_MM206_Print_Query
		/// </summary>
		[STAThread]
		private void PS_MM206_Print_Query()
		{
			string WinTitle;
			string ReportName;
			string ItmBsort;
			string ItmMsort;
			string BPLId;
			string YYYY;
			PSH_DataHelpClass dataHelpClass = new PSH_DataHelpClass();
			PSH_FormHelpClass formHelpClass = new PSH_FormHelpClass();

			try
			{
				BPLId = oForm.Items.Item("BPLId").Specific.Selected.Value.ToString().Trim();
				YYYY = oForm.Items.Item("YYYY").Specific.Value.ToString().Trim();
				ItmBsort = oForm.Items.Item("ItmBsort").Specific.Value.ToString().Trim();
				ItmMsort = oForm.Items.Item("ItmMsort").Specific.Value.ToString().Trim();

				WinTitle = "[PS_MM206] 재공수불명세서";
				ReportName = "PS_MM206_01.RPT";

				List<PSH_DataPackClass> dataPackParameter = new List<PSH_DataPackClass>();
				List<PSH_DataPackClass> dataPackFormula = new List<PSH_DataPackClass>();

				//Formula List
				dataPackFormula.Add(new PSH_DataPackClass("@BPLId", dataHelpClass.Get_ReData("BPLName", "BPLId", "OBPL", BPLId, "")));
				dataPackFormula.Add(new PSH_DataPackClass("@YYYY", YYYY));

				//Parameter
				dataPackParameter.Add(new PSH_DataPackClass("@BPLId", BPLId));
				dataPackParameter.Add(new PSH_DataPackClass("@YEAR", YYYY));
				dataPackParameter.Add(new PSH_DataPackClass("@ItmBsort", ItmBsort));
				dataPackParameter.Add(new PSH_DataPackClass("@ItmMsort", ItmMsort));

				formHelpClass.OpenCrystalReport(WinTitle, ReportName, dataPackParameter, dataPackFormula);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
		}

