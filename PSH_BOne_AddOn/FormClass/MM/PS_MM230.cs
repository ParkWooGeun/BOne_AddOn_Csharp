		/// <summary>
		/// PS_MM230_DataValidCheck
		/// </summary>
		/// <returns></returns>
		private bool PS_MM230_DataValidCheck()
		{
			bool ReturnValue = false;
			string errMessage = string.Empty;

			try
			{
				if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
				{
					PS_MM230_FormClear();
				}
				if (string.IsNullOrEmpty(oForm.Items.Item("YM").Specific.Value.ToString().Trim()))
				{
					errMessage = "년월이 입력되지 않았습니다.";
					throw new Exception();
				}
				if (string.IsNullOrEmpty(oForm.Items.Item("DocDateFr").Specific.Value.ToString().Trim()))
				{
					errMessage = "시작검수일자가 입력되지 않았습니다.";
					throw new Exception();
				}
				if (string.IsNullOrEmpty(oForm.Items.Item("DocDateTo").Specific.Value.ToString().Trim()))
				{
					errMessage = "종료검수일자가 입력되지 않았습니다.";
					throw new Exception();
				}
				if (oMat.VisualRowCount == 1)
				{
					errMessage = "라인이 존재하지 않습니다.";
					throw new Exception();
				}

				oMat.FlushToDataSource();
				oDS_PS_MM230L.RemoveRecord(oDS_PS_MM230L.Size - 1);
				oMat.LoadFromDataSource();

				if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
				{
					PS_MM230_FormClear();
				}

				ReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
			}
			return ReturnValue;
		}

		/// <summary>
		/// PS_MM230_LoadData
		/// </summary>
		private void PS_MM230_LoadData()
		{
			int i;
			string BPLId;
			string DocDateFr;
			string DocDateTo;
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				oForm.Freeze(true);

				BPLId = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
				DocDateFr = oForm.Items.Item("DocDateFr").Specific.Value.ToString().Trim();
				DocDateTo = oForm.Items.Item("DocDateTo").Specific.Value.ToString().Trim();

				sQry = " EXEC PS_MM230_05 '" + DocDateFr + "','" + DocDateTo + "','" + BPLId + "'";
				oRecordSet.DoQuery(sQry);

				oDS_PS_MM230L.Clear();
				oMat.Clear();
				oMat.FlushToDataSource();

				i = 0;
				while (!oRecordSet.EoF)
				{
					oDS_PS_MM230L.InsertRecord(i);
					oDS_PS_MM230L.SetValue("U_LineNum", i, Convert.ToString(i + 1));
					oDS_PS_MM230L.SetValue("U_CardCode", i, oRecordSet.Fields.Item(0).Value.ToString().Trim());
					oDS_PS_MM230L.SetValue("U_CardName", i, oRecordSet.Fields.Item(1).Value.ToString().Trim());
					i += 1;
					oRecordSet.MoveNext();
				}
				oMat.LoadFromDataSource();
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

