		/// <summary>
		/// PS_PP025_DelHeaderSpaceLine
		/// </summary>
		/// <returns></returns>
		private bool PS_PP025_DelHeaderSpaceLine()
		{
			bool functionReturnValue = false;
			string errMessage = string.Empty;

			try
			{ 
				if (string.IsNullOrEmpty(oDS_PS_PP025H.GetValue("U_ItmBsort", 0).ToString().Trim()))
                {
					errMessage = "거래처는 필수입력사항입니다. 확인하세요.";
					throw new Exception();
				}
				if (string.IsNullOrEmpty(oDS_PS_PP025H.GetValue("U_BPLId", 0).ToString().Trim()))
                {
					errMessage = "사업장은 필수입력사항입니다. 확인하세요.";
					throw new Exception();
				}
				if (string.IsNullOrEmpty(oDS_PS_PP025H.GetValue("U_YM", 0).ToString().Trim()))
                {
					errMessage = "년도는 필수입력사항입니다. 확인하세요.";
					throw new Exception();
                }
				functionReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
