		/// <summary>
		/// Raise_EVENT_ITEM_PRESSED
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_ITEM_PRESSED(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			string ItmBsort;
			string BPLId;
			string Code;
			string YM;
			PSH_CodeHelpClass codeHelpClass = new PSH_CodeHelpClass();

			try
			{
				if (pVal.BeforeAction == true)
				{
					if (pVal.ItemUID == "1")
					{
						if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE || oForm.Mode == SAPbouiCOM.BoFormMode.fm_UPDATE_MODE)
						{
							if (PS_PP025_DelHeaderSpaceLine() == false)
							{
								BubbleEvent = false;
								return;
							}

							if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
							{
								BPLId = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
								ItmBsort = oForm.Items.Item("ItmBsort").Specific.Value.ToString().Trim();
								YM = oForm.Items.Item("YM").Specific.Value.ToString().Trim();
								Code = codeHelpClass.Right(YM, 4) + ItmBsort + BPLId;
								oDS_PS_PP025H.SetValue("Code", 0, Code);
							}
						}
					}
				}
				else if (pVal.BeforeAction == false)
				{
					if (pVal.ItemUID == "1")
					{
						if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE && pVal.Action_Success == true)
						{
							oForm.Mode = SAPbouiCOM.BoFormMode.fm_OK_MODE;
							PSH_Globals.SBO_Application.ActivateMenuItem("1282");
						}
					}
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

		/// <summary>
		/// Raise_EVENT_KEY_DOWN
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_KEY_DOWN(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			try
			{
				if (pVal.BeforeAction == true)
				{
					if (pVal.CharPressed == 9)
					{
						if (pVal.ItemUID == "ItmBsort")
						{
							if (string.IsNullOrEmpty(oForm.Items.Item("ItmBsort").Specific.Value.ToString().Trim()))
							{
								PSH_Globals.SBO_Application.ActivateMenuItem("7425");
								BubbleEvent = false;
							}
						}
						else if (pVal.ItemUID == "Mat01")
						{
							if (pVal.ColUID == "ItemCode")
							{
								if (string.IsNullOrEmpty(oMat.Columns.Item("ItemCode").Cells.Item(pVal.Row).Specific.Value.ToString().Trim()))
								{
									PS_SM010 ChildForm01 = new PS_SM010();
									ChildForm01.LoadForm(oForm, pVal.ItemUID, pVal.ColUID, pVal.Row);
									BubbleEvent = false;
								}
							}
						}
					}
				}
				else if (pVal.BeforeAction == false)
				{
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

		/// <summary>
		/// Raise_EVENT_VALIDATE
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_VALIDATE(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			int i;
			string sQry;
			string ItemCode;
			string errMessage = string.Empty;
			double T_Quantity = 0;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				oForm.Freeze(true);

				if (pVal.BeforeAction == true)
				{
				}
				else if (pVal.BeforeAction == false)
				{
					if (pVal.ItemChanged == true)
					{
						if (pVal.ItemUID == "ItmBsort")
						{
							sQry = "SELECT Name FROM [@PSH_ITMBSORT] WHERE Code = '" + oForm.Items.Item(pVal.ItemUID).Specific.Value.ToString().Trim() + "'";
							oRecordSet.DoQuery(sQry);
							oForm.Items.Item("ItmBName").Specific.Value = oRecordSet.Fields.Item(0).Value.ToString().Trim();
						}

						if (pVal.ItemUID == "Mat01")
						{
							oMat.FlushToDataSource();
							if (pVal.ColUID == "ItemCode")
							{
								if ((pVal.Row == oMat.RowCount || oMat.VisualRowCount == 0) && !string.IsNullOrEmpty(oMat.Columns.Item("ItemCode").Cells.Item(pVal.Row).Specific.Value.ToString().Trim()))
								{
									oMat.FlushToDataSource();
									PS_PP025_AddMatrixRow(oMat.RowCount, false);
									oMat.Columns.Item("ItemCode").Cells.Item(pVal.Row).Click(SAPbouiCOM.BoCellClickType.ct_Regular);
								}

								ItemCode = oDS_PS_PP025L.GetValue("U_ItemCode", pVal.Row - 1).ToString().Trim();
								sQry = "Select ItemName ";
								sQry += " From OITM ";
								sQry += " Where ItemCode = '" + ItemCode + "'";
								oRecordSet.DoQuery(sQry);

								oMat.FlushToDataSource();

								if (oRecordSet.RecordCount == 0)
								{
									oDS_PS_PP025L.SetValue("U_ItemName", pVal.Row - 1, "");
									oMat.LoadFromDataSource();
									errMessage = "조회 결과가 없습니다. 확인하세요.";
									throw new Exception();
								}

								oDS_PS_PP025L.SetValue("U_ItemName", pVal.Row - 1, oRecordSet.Fields.Item("ItemName").Value.ToString().Trim());	//품목이름

								oMat.LoadFromDataSource();
							}
							else
							{
								for (i = 0; i <= oMat.VisualRowCount - 1; i++)
								{
									T_Quantity += Convert.ToDouble(oMat.Columns.Item("Quantity").Cells.Item(i + 1).Specific.Value.ToString().Trim());
								}

								oForm.Items.Item("T_Quantity").Specific.Value = T_Quantity;
							}
						}
					}
				}
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

