		/// <summary>
		/// PS_PP096_CheckDataValid
		/// </summary>
		/// <returns></returns>
		private bool PS_PP096_CheckDataValid()
		{
			bool functionReturnValue = false;
			string errMessage = string.Empty;

			try
			{
				if (string.IsNullOrEmpty(oForm.Items.Item("BPLId").Specific.Value.ToString().Trim()))
				{
					errMessage = "사업장은 필수입니다.";
					throw new Exception();
				}
				if (string.IsNullOrEmpty(oForm.Items.Item("DocDate").Specific.Value.ToString().Trim()))
				{
					errMessage = "작업일자는 필수입니다.";
					throw new Exception();
				}
				functionReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			return functionReturnValue;
		}

		/// <summary>
		/// PS_PP096_PrintReport01
		/// </summary>
		/// [STAThread]
		private void PS_PP096_PrintReport01()
		{
			string WinTitle;
			string ReportName;
			string sQry;
			string BPLName;
			string BPLID;
			string DocDate;
			string Div = string.Empty;
			string Msg_Renamed;

			PSH_FormHelpClass formHelpClass = new PSH_FormHelpClass();
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);
			try
			{
				BPLID       = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
				DocDate     = oForm.Items.Item("DocDate").Specific.Value.ToString().Trim();
				Msg_Renamed = oForm.Items.Item("MSG").Specific.Value.ToString().Trim();

				if (oForm.Items.Item("Rad01").Specific.Selected == true)
				{
					Div = "1";
				}
				if (oForm.Items.Item("Rad02").Specific.Selected == true)
				{
					Div = "2";
				}
				if (oForm.Items.Item("Rad03").Specific.Selected == true)
				{
					Div = "3";
				}

				sQry = "SELECT BPLName FROM [OBPL] WHERE BPLId = '" + BPLID + "'";
				oRecordSet.DoQuery(sQry);
				BPLName = oRecordSet.Fields.Item(0).Value.ToString().Trim();

				if (string.IsNullOrEmpty(BPLID))
				{
					BPLName = "전체";
				}

				WinTitle = "M/G출고계획 [PS_PP096]";
				ReportName = "PS_PP096_01.rpt";

				List<PSH_DataPackClass> dataPackFormula = new List<PSH_DataPackClass>();
				List<PSH_DataPackClass> dataPackParameter = new List<PSH_DataPackClass>();

				// Formula 수식필드
				dataPackFormula.Add(new PSH_DataPackClass("@BPLId", BPLName));
				dataPackFormula.Add(new PSH_DataPackClass("@DocDate", DocDate.Substring(0, 4) + "-" + DocDate.Substring(4, 2) + "-" + DocDate.Substring(6, 2)));
				dataPackFormula.Add(new PSH_DataPackClass("@Div", Div));

				// Parameter
				dataPackParameter.Add(new PSH_DataPackClass("@BPLId", BPLID));
				dataPackParameter.Add(new PSH_DataPackClass("@DocDate", DocDate));
				dataPackParameter.Add(new PSH_DataPackClass("@Div", Div));
				dataPackParameter.Add(new PSH_DataPackClass("@msg", Msg_Renamed));

				formHelpClass.CrystalReportOpen(WinTitle, ReportName, dataPackParameter, dataPackFormula);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
			}
		}

