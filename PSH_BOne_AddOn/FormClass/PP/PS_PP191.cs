		/// <summary>
		/// PS_PP191_MTX01
		/// </summary>
		private void PS_PP191_MTX01()
		{
			int loopCount;
			string sQry;
			string errMessage = string.Empty;

			string BPLID;	 //사업장
			string ToolType; //금형구분
			string Gubun;	 //구분 1:전체 2:폐기제외
			string Item;	 //아이템
			string CallSize; //호칭

			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);
			SAPbouiCOM.ProgressBar ProgressBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);

			try
			{
				BPLID = oForm.Items.Item("BPLId").Specific.Selected.Value.ToString().Trim();
				ToolType = oForm.Items.Item("ToolType").Specific.Selected.Value.ToString().Trim();
				Gubun = oForm.Items.Item("Gubun").Specific.Selected.Value.ToString().Trim();
				Item = oForm.Items.Item("Item").Specific.Value.ToString().Trim();
				CallSize = oForm.Items.Item("CallSize").Specific.Value.ToString().Trim();

				ProgressBar01.Text = "조회시작!";

				oForm.Freeze(true);

				sQry = "EXEC PS_PP191_01 '" + BPLID + "','" + ToolType + "','" + Gubun + "','" + Item + "','" + CallSize + "'";
				oRecordSet.DoQuery(sQry);

				oMat01.Clear();
				oMat01.FlushToDataSource();
				oMat01.LoadFromDataSource();

				if (oRecordSet.RecordCount == 0)
				{
					oMat01.Clear();
					errMessage = "결과가 존재하지 않습니다.";
					throw new Exception();
				}

				for (loopCount = 0; loopCount <= oRecordSet.RecordCount - 1; loopCount++)
				{
					if (loopCount != 0)
					{
						oDS_PS_PP191L.InsertRecord(loopCount);
					}
					oDS_PS_PP191L.Offset = loopCount;
					oDS_PS_PP191L.SetValue("U_LineNum", loopCount, Convert.ToString(loopCount + 1));
					oDS_PS_PP191L.SetValue("U_ColReg01", loopCount, oRecordSet.Fields.Item("Code").Value.ToString().Trim());
					oDS_PS_PP191L.SetValue("U_ColReg02", loopCount, oRecordSet.Fields.Item("Item").Value.ToString().Trim());
					oDS_PS_PP191L.SetValue("U_ColReg03", loopCount, oRecordSet.Fields.Item("CallSize").Value.ToString().Trim());
					oDS_PS_PP191L.SetValue("U_ColReg04", loopCount, oRecordSet.Fields.Item("Remark").Value.ToString().Trim());
					oDS_PS_PP191L.SetValue("U_ColReg05", loopCount, oRecordSet.Fields.Item("Statnm").Value.ToString().Trim());
					oRecordSet.MoveNext();

					ProgressBar01.Value += 1;
					ProgressBar01.Text = ProgressBar01.Value + "/" + oRecordSet.RecordCount + "건 조회중...!";
				}
				oMat01.LoadFromDataSource();
				oMat01.AutoResizeColumns();
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			finally
			{
		        ProgressBar01.Stop();
				System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgressBar01);
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

