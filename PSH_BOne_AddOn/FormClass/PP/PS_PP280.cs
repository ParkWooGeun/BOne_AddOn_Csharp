		/// <summary>
		/// PS_PP280_CheckDataValid
		/// </summary>
		/// <returns></returns>
		private bool PS_PP280_CheckDataValid()
		{
			bool functionReturnValue = false;

			int i;
			int j;
			string errMessage = string.Empty;

			try
			{
				if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
				{
					PS_PP280_ClearForm();
				}

				if (oMat.VisualRowCount == 1)
				{
					errMessage = "라인이 존재하지 않습니다.";
					throw new Exception();
				}

				for (i = 1; i <= oMat.VisualRowCount - 1; i++)
				{
					if (string.IsNullOrEmpty(oMat.Columns.Item("Code1").Cells.Item(i).Specific.Value.ToString().Trim()))
					{
						errMessage = "설비코드는 필수입니다.";
						throw new Exception();
					}

					if (string.IsNullOrEmpty(oMat.Columns.Item("Name1").Cells.Item(i).Specific.Value.ToString().Trim()))
					{
						errMessage = "항목명은 필수입니다.";
						throw new Exception();
					}

					if (string.IsNullOrEmpty(oMat.Columns.Item("UseYN").Cells.Item(i).Specific.Value.ToString().Trim()))
					{
						errMessage = "사용유무는 필수입니다.";
						throw new Exception();
					}

					//화면 내에서의 항목코드 중복 검사
					for (j = i + 1; j <= oMat.VisualRowCount - 1; j++)
					{
						if (oMat.Columns.Item("Code1").Cells.Item(i).Specific.Value.ToString().Trim() == oMat.Columns.Item("Code1").Cells.Item(j).Specific.Value.ToString().Trim())
						{
							errMessage = "설비코드가 중복 되었습니다.";
							throw new Exception();
						}
					}
				}

				oMat.FlushToDataSource();
				oDS_PS_PP280L.RemoveRecord(oDS_PS_PP280L.Size - 1);
				oMat.LoadFromDataSource();

				if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE)
				{
					PS_PP280_ClearForm();
				}

				functionReturnValue = true;
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			return functionReturnValue;
		}

