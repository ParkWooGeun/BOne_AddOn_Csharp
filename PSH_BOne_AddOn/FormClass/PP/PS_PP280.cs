				oForm.Freeze(true);

				if (pVal.BeforeAction == true)
				{
					if (pVal.ItemChanged == true)
					{
						oMat.FlushToDataSource();

						if (pVal.ItemUID == "Mat01")
						{
							if (pVal.ColUID == "Code1")
							{
								oDS_PS_PP280L.SetValue("U_" + pVal.ColUID, pVal.Row - 1, oMat.Columns.Item(pVal.ColUID).Cells.Item(pVal.Row).Specific.Value.ToString().Trim());
								if ((oMat.RowCount == pVal.Row) && !string.IsNullOrEmpty(oDS_PS_PP280L.GetValue("U_" + pVal.ColUID, pVal.Row - 1).ToString().Trim()))
								{
									PS_PP280_AddMatrixRow(pVal.Row, false);
								}
							}
							else if (pVal.ColUID == "UseYN")
							{
								oDS_PS_PP280L.SetValue("U_" + pVal.ColUID, pVal.Row - 1, oMat.Columns.Item(pVal.ColUID).Cells.Item(pVal.Row).Specific.Selected.Value.ToString().Trim());
							}
							else
							{
								oDS_PS_PP280L.SetValue("U_" + pVal.ColUID, pVal.Row - 1, oMat.Columns.Item(pVal.ColUID).Cells.Item(pVal.Row).Specific.Value.ToString().Trim());
							}

							oMat.Columns.Item("UseYN").Cells.Item(pVal.Row).Specific.Select(0, SAPbouiCOM.BoSearchKey.psk_Index); //기본으로 'Y' 세팅
							oMat.Columns.Item(pVal.ColUID).Cells.Item(pVal.Row).Click(SAPbouiCOM.BoCellClickType.ct_Regular);
						}

						//담당명
						if (pVal.ColUID == "RspCode")
						{
							sQry = " SELECT      U_CdName ";
							sQry += " FROM        [@PS_SY001L]";
							sQry += " WHERE       Code = 'P003'";
							sQry += "   AND       U_Minor = '" + oMat.Columns.Item("RspCode").Cells.Item(pVal.Row).Specific.Value.ToString().Trim() + "'";
							oRecordSet.DoQuery(sQry);
							oMat.Columns.Item("RspName").Cells.Item(pVal.Row).Specific.Value = oRecordSet.Fields.Item(0).Value.ToString().Trim();
						}
					}
				}
				else if (pVal.BeforeAction == false)
				{
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

