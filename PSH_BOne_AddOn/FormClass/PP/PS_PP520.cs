		/// <summary>
		/// PS_PP520_PrintReport
		/// </summary>
		[STAThread]
		private void PS_PP520_PrintReport()
		{
			string WinTitle;
			string ReportName = string.Empty;
			string BPLID;
			string CardCode;
			string Pumtxt;
			string DocDateFr;
			string DocDateTo;
			string InOutGbn;

			PSH_FormHelpClass formHelpClass = new PSH_FormHelpClass();

			try
			{
				BPLID = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
				CardCode  = oForm.Items.Item("CardCode").Specific.Value.ToString().Trim();
				Pumtxt    = oForm.Items.Item("Pumtxt").Specific.Value.ToString().Trim();
				DocDateFr = oForm.Items.Item("DocDateFr").Specific.Value.ToString().Trim();
				DocDateTo = oForm.Items.Item("DocDateTo").Specific.Value.ToString().Trim();
				InOutGbn  = oForm.Items.Item("InOutGbn").Specific.Value.ToString().Trim();

				if (string.IsNullOrEmpty(CardCode))
				{
					CardCode = "%";
				}
				if (string.IsNullOrEmpty(Pumtxt))
				{
					Pumtxt = "%";
				}

				WinTitle = "[PS_PP520_01] 생산실적";

				if (oForm.DataSources.UserDataSources.Item("RadioBtn").Value == "D")
				{
					ReportName = "PS_PP520_01.RPT";
				}
				else if (oForm.DataSources.UserDataSources.Item("RadioBtn").Value == "A")
				{
					ReportName = "PS_PP520_02.RPT";
				}
				else if (oForm.DataSources.UserDataSources.Item("RadioBtn").Value == "B")
				{
					ReportName = "PS_PP520_03.RPT";
				}
				else if (oForm.DataSources.UserDataSources.Item("RadioBtn").Value == "C")
				{
					ReportName = "PS_PP520_04.RPT";
				}

				List<PSH_DataPackClass> dataPackFormula = new List<PSH_DataPackClass>();
				List<PSH_DataPackClass> dataPackParameter = new List<PSH_DataPackClass>();

				// Formula 수식필드
				dataPackFormula.Add(new PSH_DataPackClass("@DocDateFr", DocDateFr.Substring(0, 4) + "-" + DocDateFr.Substring(4, 2) + "-" + DocDateFr.Substring(6, 2)));
				dataPackFormula.Add(new PSH_DataPackClass("@DocDateTo", DocDateTo.Substring(0, 4) + "-" + DocDateTo.Substring(4, 2) + "-" + DocDateTo.Substring(6, 2)));

				// Parameter
				if (oForm.DataSources.UserDataSources.Item("RadioBtn").Value == "B")
				{
					dataPackParameter.Add(new PSH_DataPackClass("@BPLId", BPLID));
					dataPackParameter.Add(new PSH_DataPackClass("@Minor", Pumtxt));
					dataPackParameter.Add(new PSH_DataPackClass("@DocDateFr", DocDateFr));
					dataPackParameter.Add(new PSH_DataPackClass("@DocDateTo", DocDateTo));
					dataPackParameter.Add(new PSH_DataPackClass("@InOutGbn", InOutGbn));
				}
				else
                {
					dataPackParameter.Add(new PSH_DataPackClass("@BPLId", BPLID));
					dataPackParameter.Add(new PSH_DataPackClass("@CardCode", CardCode));
					dataPackParameter.Add(new PSH_DataPackClass("@Minor", Pumtxt));
					dataPackParameter.Add(new PSH_DataPackClass("@DocDateFr", DocDateFr));
					dataPackParameter.Add(new PSH_DataPackClass("@DocDateTo", DocDateTo));
					dataPackParameter.Add(new PSH_DataPackClass("@InOutGbn", InOutGbn));
				}

				formHelpClass.CrystalReportOpen(WinTitle, ReportName, dataPackParameter, dataPackFormula);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

