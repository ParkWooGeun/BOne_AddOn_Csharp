			}
		}

		/// <summary>
		/// PS_PP902_MTX01
		/// </summary>
		private void PS_PP902_MTX01()
		{
			string sQry = string.Empty;
			string errMessage = string.Empty;
			string BPLId;
			string DocDateFr;
			string DocDateTo;
			string Div;
			SAPbouiCOM.ProgressBar ProgressBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);

			try
			{
				oForm.Freeze(true);

				BPLId = oForm.Items.Item("BPLId").Specific.Selected.Value.ToString().Trim();
				DocDateFr = oForm.Items.Item("DocDatefr").Specific.Value.ToString().Trim();
				DocDateTo = oForm.Items.Item("DocDateto").Specific.Value.ToString().Trim();
				Div       = oForm.Items.Item("Div").Specific.Selected.Value.ToString().Trim();

				ProgressBar01.Text = "조회중...";

				if (Div == "1")
				{
					sQry = "EXEC PS_PP902_01 '" + BPLId + "','" + DocDateTo + "'";
				}
				else if (Div == "2")
				{
					sQry = "EXEC PS_PP902_02 '" + BPLId + "','" + DocDateFr + "'";
				}

				oGrid.DataTable.Clear();

				oForm.DataSources.DataTables.Item("DataTable").ExecuteQuery(sQry);
				oGrid.DataTable = oForm.DataSources.DataTables.Item("DataTable");

				if (oGrid.Rows.Count == 0)
				{
					errMessage = "결과가 존재하지 않습니다.";
					throw new Exception();
				}
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			finally
			{
				oGrid.AutoResizeColumns();
				oForm.Update();
				ProgressBar01.Stop();
				System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgressBar01);
				oForm.Freeze(false);
			}
		}

