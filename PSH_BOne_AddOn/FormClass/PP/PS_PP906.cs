		{
			try
			{
				oForm.Freeze(true);

				if (pVal.BeforeAction == true)
				{
				}
				else if (pVal.BeforeAction == false)
				{
					if (pVal.ItemUID == "Gubun")
					{
						if (oForm.Items.Item("Gubun").Specific.VALUE == "2")
						{
							oForm.Items.Item("Div").Enabled = false;
