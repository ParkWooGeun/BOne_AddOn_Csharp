		/// <summary>
		/// PS_PP907_MTX01
		/// </summary>
		/// <param name="prmBPLId"></param>
		/// <param name="prmCntcCode"></param>
		/// <param name="prmFrDt"></param>
		/// <param name="prmToDt"></param>
		private void PS_PP907_MTX01(string prmBPLId, string prmCntcCode, string prmFrDt, string prmToDt)
		{
			string sQry;
			short loopCount1;
			string errMessage = String.Empty;

			string Param01;
			string Param02;
			string Param03;
			string Param04;

			try
			{
				oForm.Freeze(true);

				Param01 = prmBPLId;
				Param02 = prmCntcCode;
				Param03 = prmFrDt;
				Param04 = prmToDt;

				sQry = "EXEC PS_PP907_01 '" + Param01 + "','" + Param02 + "','" + Param03 + "','" + Param04 + "'";

				oGrid.DataTable.Clear();

				oForm.DataSources.DataTables.Item("DataTable").ExecuteQuery(sQry);
				oGrid.DataTable = oForm.DataSources.DataTables.Item("DataTable");

				for (loopCount1 = 4; loopCount1 <= oGrid.Columns.Count - 1; loopCount1++)
				{
					oGrid.Columns.Item(loopCount1).RightJustified = true;
				}

				if (oGrid.Rows.Count == 0)
				{
					errMessage = "결과가 존재하지 않습니다.";
					throw new Exception();
				}
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			finally
			{
				oGrid.AutoResizeColumns();
				oForm.Update();
				oForm.Freeze(false);
			}
		}

