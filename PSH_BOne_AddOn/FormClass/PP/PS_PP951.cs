		/// <summary>
		/// Raise_EVENT_ITEM_PRESSED
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_ITEM_PRESSED(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			try
			{
				if (pVal.BeforeAction == true)
				{
					if (pVal.ItemUID == "1")
					{
						if (oForm.Mode == SAPbouiCOM.BoFormMode.fm_ADD_MODE && pVal.Action_Success == true)
						{
							oForm.Mode = SAPbouiCOM.BoFormMode.fm_OK_MODE;
							PSH_Globals.SBO_Application.ActivateMenuItem("1282");
							LoadData();
						}
					}
					else if (pVal.ItemUID == "Btn01")
					{
						LoadData();
					}
				}
				else if (pVal.BeforeAction == false)
				{
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

		/// <summary>
		/// Raise_EVENT_DOUBLE_CLICK
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_DOUBLE_CLICK(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			string BPLId;
			string YYYYMM;
			string ItmBsort;
			string errMessage = string.Empty;

			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				oForm.Freeze(true);

				if (pVal.BeforeAction == true)
				{
					if (pVal.ItemUID == "Grid01")
					{
						for (int i = 0; i <= oGrid01.Rows.Count - 1; i++)
						{
							if (oGrid01.Rows.IsSelected(i) == true)
							{
								BPLId    = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
								YYYYMM   = oForm.Items.Item("YYYYMM").Specific.Value.ToString().Trim();
								ItmBsort = oGrid01.DataTable.GetValue(0, i).ToString().Trim();
								sQry = "EXEC [PS_PP951_01] '" + BPLId + "', '" + YYYYMM + "', '" + ItmBsort + "', '20'";
								oRecordSet.DoQuery(sQry);

								if ((oRecordSet.RecordCount == 0))
								{
									errMessage = "조회 결과가 없습니다. 확인하세요.:";
									throw new Exception();
								}
								oForm.DataSources.DataTables.Item(1).ExecuteQuery((sQry));
								oGrid02.DataTable = oForm.DataSources.DataTables.Item("PS_USERDS02");
							}
						}
						BubbleEvent = false;
					}
				}
				else if (pVal.BeforeAction == false)
				{
				}
			}
			catch (Exception ex)
			{
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
				}
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

		/// <summary>
		/// Raise_EVENT_VALIDATE
		/// </summary>
		/// <param name="FormUID"></param>
		/// <param name="pVal"></param>
		/// <param name="BubbleEvent"></param>
		private void Raise_EVENT_VALIDATE(string FormUID, ref SAPbouiCOM.ItemEvent pVal, ref bool BubbleEvent)
		{
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				oForm.Freeze(true);

				if (pVal.BeforeAction == true)
				{
				}
				else if (pVal.BeforeAction == false)
				{
					if (pVal.ItemChanged == true)
					{
						if (pVal.ItemUID == "ItemCode")
						{
							sQry = "Select FrgnName, U_Size, U_Mark, U_ItemType From OITM Where ItemCode = '" + oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim() + "'";
							oRecordSet.DoQuery(sQry);
							oForm.Items.Item("ItemName").Specific.Value = oRecordSet.Fields.Item("FrgnName").Value.ToString().Trim();
							oForm.Items.Item("Size").Specific.Value = oRecordSet.Fields.Item("U_Size").Value.ToString().Trim();
							oForm.Items.Item("Mark").Specific.Select(oRecordSet.Fields.Item("U_Mark").Value.ToString().Trim());
							oForm.Items.Item("ItemType").Specific.Select(oRecordSet.Fields.Item("U_ItemType").Value.ToString().Trim());
						}
					}
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

