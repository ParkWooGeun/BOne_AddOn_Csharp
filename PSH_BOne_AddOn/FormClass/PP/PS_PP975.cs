		/// <summary>
		/// PS_PP975_SetComboBox
		/// </summary>
		private void PS_PP975_SetComboBox()
		{
			PSH_DataHelpClass dataHelpClass = new PSH_DataHelpClass();

			try
			{
				oForm.Items.Item("TXT1").Specific.ValidValues.Add("", "");
				dataHelpClass.Set_ComboList(oForm.Items.Item("TXT1").Specific, "SELECT b.U_CdName, b.U_Minor FROM [@PS_SY001H] a Inner Join [@PS_SY001L] b On a.Code = b.Code Where a.Code = 'P021' ORDER BY b.U_Minor", "", false, false);

				oForm.Items.Item("TXT3").Specific.ValidValues.Add("", "");
