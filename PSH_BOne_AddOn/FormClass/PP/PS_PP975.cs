		}

		/// <summary>
		/// PS_PP975_PrintReport
		/// </summary>
		[STAThread]
		private void PS_PP975_PrintReport()
		{
			string WinTitle;
			string ReportName;
			string TXT1;
			string TXT2;
			string TXT3;
			int FNO;
			int TNO;
			PSH_FormHelpClass formHelpClass = new PSH_FormHelpClass();

			try
			{
				TXT1 = oForm.Items.Item("TXT1").Specific.Value.ToString().Trim();
				TXT2 = oForm.Items.Item("TXT2").Specific.Value.ToString().Trim();
				TXT3 = oForm.Items.Item("TXT3").Specific.Value.ToString().Trim();
				FNO = Convert.ToInt16(oForm.Items.Item("FNO").Specific.Value.ToString().Trim());
				TNO = Convert.ToInt16(oForm.Items.Item("TNO").Specific.Value.ToString().Trim());

				WinTitle = "M/G SHIPPING MARK출력[PS_PP975_01]";
				ReportName = "PS_PP975_01.RPT";

				List<PSH_DataPackClass> dataPackParameter = new List<PSH_DataPackClass>();

				// Formula 수식필드

				// Parameter
				dataPackParameter.Add(new PSH_DataPackClass("@TXT1", TXT1));
				dataPackParameter.Add(new PSH_DataPackClass("@TXT2", TXT2));
				dataPackParameter.Add(new PSH_DataPackClass("@TXT3", TXT3));
				dataPackParameter.Add(new PSH_DataPackClass("@CNT1", FNO));
				dataPackParameter.Add(new PSH_DataPackClass("@CNT2", TNO));

				formHelpClass.OpenCrystalReport(WinTitle, ReportName, dataPackParameter);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

