		/// <summary>
		/// PS_PP996_FlushToItemValue
		/// </summary>
		/// <param name="oUID"></param>
		/// <param name="oRow"></param>
		/// <param name="oCol"></param>
		private void PS_PP996_FlushToItemValue(string oUID, int oRow, string oCol)
		{
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				switch (oUID)
				{
					case "ItemCode":
						sQry = "SELECT FrgnName FROM OITM WHERE ItemCode =  '" + oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim() + "'";
						oRecordSet.DoQuery(sQry);
						oForm.Items.Item("U_ItemName").Specific.String = oRecordSet.Fields.Item("FrgnName").Value.ToString().Trim();

						sQry = "SELECT U_SIZE FROM OITM WHERE ItemCode =  '" + oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim() + "'";
						oRecordSet.DoQuery(sQry);
						oForm.Items.Item("CodeName").Specific.String = oRecordSet.Fields.Item("U_SIZE").Value.ToString().Trim();
						break;

					case "Customer":
						sQry = "select cardname from ocrd where cardcode = '" + oForm.Items.Item("Customer").Specific.Value.ToString().Trim() + "'";
						oRecordSet.DoQuery(sQry);
						oForm.Items.Item("CustName").Specific.String = oRecordSet.Fields.Item("cardname").Value.ToString().Trim();
						break;
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
			}
		}

		/// <summary>
		/// PS_PP996_PrintReport
		/// </summary>
		[STAThread]
		private void PS_PP996_PrintReport()
		{
			string WinTitle;
			string ReportName;
			string Title;
			string ItemCode;
			string ItemName;
			string CodeName;
			string Customer;
			string CustName;
			int PackingNo1;
			int PackingNo2;
			double Gwgt;
			double Nwgt;

			PSH_FormHelpClass formHelpClass = new PSH_FormHelpClass();

			try
			{
				Title    = oForm.Items.Item("Title").Specific.Value.ToString().Trim();
				ItemCode = oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim();
				ItemName = oForm.Items.Item("U_ItemName").Specific.Value.ToString().Trim();
				CodeName = oForm.Items.Item("CodeName").Specific.Value.ToString().Trim();
				Customer = oForm.Items.Item("Customer").Specific.Value.ToString().Trim();
				CustName = oForm.Items.Item("CustName").Specific.Value.ToString().Trim();
				PackingNo1 = Convert.ToInt32(oForm.Items.Item("PackingNo1").Specific.Value.ToString().Trim());
				PackingNo2 = Convert.ToInt32(oForm.Items.Item("PackingNo2").Specific.Value.ToString().Trim());
				Gwgt = Convert.ToDouble(oForm.Items.Item("Gwgt").Specific.Value.ToString().Trim());
				Nwgt = Convert.ToDouble(oForm.Items.Item("Nwgt").Specific.Value.ToString().Trim());

				WinTitle = "분말LABEL출력2[PS_PP996_01]";
				ReportName = "PS_PP996_01.RPT";

				List<PSH_DataPackClass> dataPackParameter = new List<PSH_DataPackClass>();

				// Formula 수식필드

				// Parameter
				dataPackParameter.Add(new PSH_DataPackClass("@Title", Title));
				dataPackParameter.Add(new PSH_DataPackClass("@ItemCode", ItemCode));
				dataPackParameter.Add(new PSH_DataPackClass("@ItemName", ItemName));
				dataPackParameter.Add(new PSH_DataPackClass("@CodeName", CodeName));
				dataPackParameter.Add(new PSH_DataPackClass("@Customer", Customer));
				dataPackParameter.Add(new PSH_DataPackClass("@CustName", CustName));
				dataPackParameter.Add(new PSH_DataPackClass("@Gwgt", Gwgt));
				dataPackParameter.Add(new PSH_DataPackClass("@Nwgt", Nwgt));
				dataPackParameter.Add(new PSH_DataPackClass("@PackingNo1", PackingNo1));
				dataPackParameter.Add(new PSH_DataPackClass("@PackingNo2", PackingNo2));

				formHelpClass.OpenCrystalReport(WinTitle, ReportName, dataPackParameter);
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.StatusBar.SetText(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message, BoMessageTime.bmt_Short, BoStatusBarMessageType.smt_Error);
			}
		}

