		/// <summary>
		/// Form 호출
		/// </summary>
		public override void LoadForm(string oFormDocEntry)
		{
			int i;
			MSXML2.DOMDocument oXmlDoc = new MSXML2.DOMDocument();

			try
			{
				oXmlDoc.load(PSH_Globals.SP_Path + "\\" + PSH_Globals.Screen + "\\PS_QM220.srf");
				oXmlDoc.selectSingleNode("Application/forms/action/form/@uid").nodeValue = oXmlDoc.selectSingleNode("Application/forms/action/form/@uid").nodeValue + "_" + (SubMain.Get_TotalFormsCount());
				oXmlDoc.selectSingleNode("Application/forms/action/form/@top").nodeValue = Convert.ToInt32(oXmlDoc.selectSingleNode("Application/forms/action/form/@top").nodeValue.ToString()) + (SubMain.Get_CurrentFormsCount() * 10);
				oXmlDoc.selectSingleNode("Application/forms/action/form/@left").nodeValue = Convert.ToInt32(oXmlDoc.selectSingleNode("Application/forms/action/form/@left").nodeValue.ToString()) + (SubMain.Get_CurrentFormsCount() * 10);

				for (i = 1; i <= (oXmlDoc.selectNodes("Application/forms/action/form/items/action/item/specific/@titleHeight").length); i++)
				{
					oXmlDoc.selectNodes("Application/forms/action/form/items/action/item/specific/@titleHeight")[i - 1].nodeValue = 20;
					oXmlDoc.selectNodes("Application/forms/action/form/items/action/item/specific/@cellHeight")[i - 1].nodeValue = 16;
				}

				oFormUniqueID = "PS_QM220_" + SubMain.Get_TotalFormsCount();
				SubMain.Add_Forms(this, oFormUniqueID, "PS_QM220");

				PSH_Globals.SBO_Application.LoadBatchActions(oXmlDoc.xml.ToString());
				oForm = PSH_Globals.SBO_Application.Forms.Item(oFormUniqueID);

				oForm.SupportedModes = -1;
				oForm.Mode = SAPbouiCOM.BoFormMode.fm_ADD_MODE;

				oForm.Freeze(true);

				PS_QM220_CreateItems();
				PS_QM220_ComboBox_Setting();

				oForm.EnableMenu("1281", false); // 찾기
				oForm.EnableMenu("1282", false); // 추가
				oForm.EnableMenu("1283", false); // 삭제
				oForm.EnableMenu("1286", false); // 닫기
				oForm.EnableMenu("1287", false); // 복제
				oForm.EnableMenu("1285", false); // 복원
				oForm.EnableMenu("1284", false); // 취소
				oForm.EnableMenu("1293", false); // 행삭제
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				oForm.Update();
				oForm.Freeze(false);
				oForm.Visible = true;
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oXmlDoc); //메모리 해제
			}
		}

		/// <summary>
		/// PS_QM220_CreateItems
		/// </summary>
		private void PS_QM220_CreateItems()
		{
			try
			{
				oDS_PS_TEMPTABLE = oForm.DataSources.DBDataSources.Item("@PS_USERDS01");
				oMat = oForm.Items.Item("Mat01").Specific;
				oMat.SelectionMode = SAPbouiCOM.BoMatrixSelect.ms_Auto;
				oMat.AutoResizeColumns();

				oForm.DataSources.UserDataSources.Add("DocDateFr", SAPbouiCOM.BoDataType.dt_DATE, 8);
				oForm.Items.Item("DocDateFr").Specific.DataBind.SetBound(true, "", "DocDateFr");
				oForm.DataSources.UserDataSources.Item("DocDateFr").Value = DateTime.Now.ToString("yyyyMM") + "01";

				oForm.DataSources.UserDataSources.Add("DocDateTo", SAPbouiCOM.BoDataType.dt_DATE, 8);
				oForm.Items.Item("DocDateTo").Specific.DataBind.SetBound(true, "", "DocDateTo");
				oForm.DataSources.UserDataSources.Item("DocDateTo").Value = DateTime.Now.ToString("yyyyMMdd");
			}

			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
		}

		/// <summary>
		/// PS_QM220_ComboBox_Setting
		/// </summary>
		private void PS_QM220_ComboBox_Setting()
		{
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);
			PSH_DataHelpClass dataHelpClass = new PSH_DataHelpClass();
			try
			{
				sQry = "SELECT BPLId, BPLName From [OBPL] order by 1";
				oRecordSet.DoQuery(sQry);
				while (!oRecordSet.EoF)
				{
					oForm.Items.Item("BPLId").Specific.ValidValues.Add(oRecordSet.Fields.Item(0).Value.ToString().Trim(), oRecordSet.Fields.Item(1).Value.ToString().Trim());
					oRecordSet.MoveNext();
				}
				oForm.Items.Item("BPLId").Specific.Select(dataHelpClass.User_BPLID(), SAPbouiCOM.BoSearchKey.psk_ByValue);

			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
			}
		}

		/// <summary>
		/// PS_QM220_AddMatrixRow
		/// </summary>
		/// <param name="oRow"></param>
		/// <param name="RowIserted"></param>
		private void PS_QM220_AddMatrixRow(int oRow, bool RowIserted)
		{
			try
			{
				oForm.Freeze(true);
				if (RowIserted == false)
				{
					oDS_PS_TEMPTABLE.InsertRecord(oRow);
				}
				oMat.AddRow();
				oDS_PS_TEMPTABLE.Offset = oRow;
				oDS_PS_TEMPTABLE.SetValue("U_LineNum", oRow, Convert.ToString(oRow + 1));
				oMat.LoadFromDataSource();
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				oForm.Freeze(false);
			}
		}

		/// <summary>
		/// PS_QM220_Display_MatrixData
		/// </summary>
		private void PS_QM220_Display_MatrixData()
		{
			string ItemCode;
			string DocDateFr;
			string BPLID;
			string DocDateTo;
			string CardCode;
			int sCnt;
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);
			SAPbouiCOM.ProgressBar ProgressBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);

			try
			{
				oForm.Freeze(true);
				BPLID = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
				DocDateFr = oForm.Items.Item("DocDateFr").Specific.Value.ToString().Trim();
				DocDateTo = oForm.Items.Item("DocDateTo").Specific.Value.ToString().Trim();
				ItemCode = oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim();
				CardCode = oForm.Items.Item("CardCode").Specific.Value.ToString().Trim();

				if (string.IsNullOrEmpty(ItemCode))
                {
					ItemCode = "%";
				}
				if (string.IsNullOrEmpty(CardCode))
                {
					CardCode = "%";
				}

				ProgressBar01.Text = "조회시작!";

				sQry = "EXEC [PS_QM220_01] '" + BPLID + "', '" + DocDateFr + "', '" + DocDateTo + "', '" + ItemCode + "', '" + CardCode + "'";
				oRecordSet.DoQuery(sQry);

				oDS_PS_TEMPTABLE.Clear();

				sCnt = 0;
				if (oRecordSet.RecordCount > 0)
				{
					while (!oRecordSet.EoF)
					{
						oDS_PS_TEMPTABLE.InsertRecord(sCnt);
						oDS_PS_TEMPTABLE.Offset = sCnt;

						oDS_PS_TEMPTABLE.SetValue("U_LineNum", sCnt, Convert.ToString(sCnt + 1));
						oDS_PS_TEMPTABLE.SetValue("U_ColReg01", sCnt, oRecordSet.Fields.Item(0).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg02", sCnt, oRecordSet.Fields.Item(1).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg03", sCnt, oRecordSet.Fields.Item(2).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg04", sCnt, Convert.ToDateTime(oRecordSet.Fields.Item(3).Value.ToString().Trim()).ToString("yyyyMMdd"));
						oDS_PS_TEMPTABLE.SetValue("U_ColReg05", sCnt, oRecordSet.Fields.Item(4).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg06", sCnt, oRecordSet.Fields.Item(5).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg07", sCnt, oRecordSet.Fields.Item(6).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg08", sCnt, oRecordSet.Fields.Item(7).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg09", sCnt, oRecordSet.Fields.Item(8).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg10", sCnt, oRecordSet.Fields.Item(9).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg11", sCnt, oRecordSet.Fields.Item(10).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg12", sCnt, oRecordSet.Fields.Item(11).Value.ToString().Trim());
						oDS_PS_TEMPTABLE.SetValue("U_ColReg13", sCnt, oRecordSet.Fields.Item(12).Value.ToString().Trim());
						sCnt += 1;
						oRecordSet.MoveNext();

						ProgressBar01.Value += 1;
						ProgressBar01.Text = ProgressBar01.Value + "/" + oRecordSet.RecordCount + "건 조회중...!";
					}
				}

				oMat.LoadFromDataSource();
			}
			catch (Exception ex)
			{
				if (ProgressBar01 != null)
				{
					ProgressBar01.Stop();
				}
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				ProgressBar01.Stop();
				System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgressBar01);
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

