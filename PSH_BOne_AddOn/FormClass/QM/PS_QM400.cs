		/// <summary>
		/// PS_QM400_FlushToItemValue
		/// </summary>
		/// <param name="oUID"></param>
		/// <param name="oRow"></param>
		/// <param name="oCol"></param>
		private void PS_QM400_FlushToItemValue(string oUID, int oRow, string oCol)
		{
			string ReviNo; //개정번호
			string DrawNo; //도면번호
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				switch (oUID)
				{
					case "Mat01":
						oMat.FlushToDataSource();
						if (oMat.RowCount == oRow && !string.IsNullOrEmpty(oDS_PS_QM400L.GetValue("U_ChkPnt", oRow - 1).ToString().Trim()))
						{
							PS_QM400_AddMatrixRow(oRow, false);
						}
						break;

					case "DrawNo":
						DrawNo = oForm.Items.Item("DrawNo").Specific.Value.ToString().Trim();

						//도면번호 존재 여부 조회
						sQry = "    SELECT      ISNULL(MAX(U_ReviNo), 0) AS [ReviNo]";
						sQry += " FROM       [@PS_QM400H]";
						sQry += " WHERE     U_DrawNo = '" + DrawNo + "'";
						sQry += "                AND Status = 'O'";
						oRecordSet.DoQuery(sQry);

						if (Convert.ToInt32(oRecordSet.Fields.Item("ReviNo").Value.ToString().Trim()) == 0)
						{
							ReviNo = "1";
						}
						else
						{
							ReviNo = Convert.ToString(Convert.ToInt32(oRecordSet.Fields.Item("ReviNo").Value.ToString().Trim()) + 1);
						}

						oDS_PS_QM400H.SetValue("U_ReviNo", 0, ReviNo);
						break;
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
			}
		}

