				oRecordSet.DoQuery(sQry);

				//임시테이블에 check된항목저장
				oMat.FlushToDataSource();
				for (i = 0; i <= oMat.VisualRowCount - 1; i++)
				{
					if (oDS_PS_QM430L.GetValue("U_ColReg01", i).ToString().Trim() == "Y")
					{
						sQry = "INSERT INTO [Z_PS_QM430] VALUES ('" + CntcCode + "', '" + oDS_PS_QM430L.GetValue("U_ColReg02", i).ToString().Trim() + "')";
