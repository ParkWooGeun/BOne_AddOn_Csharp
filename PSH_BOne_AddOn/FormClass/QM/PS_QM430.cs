		/// <summary>
		/// PS_QM430_MTX01
		/// </summary>
		private void PS_QM430_MTX01()
		{
			int i;
			string ChkDateF; //검사일자(시작)
			string ChkDateT; //검사일자(종료)
			string CardCode; //납품처
			string ItemCode; //작번
			string DrawNo;   //도면번호
			string errMessage = string.Empty;
			SAPbouiCOM.ProgressBar ProgressBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				oForm.Freeze(true);
				ChkDateF = oForm.Items.Item("ChkDateF").Specific.Value.ToString().Trim();
				ChkDateT = oForm.Items.Item("ChkDateT").Specific.Value.ToString().Trim();
				CardCode = oForm.Items.Item("CardCode").Specific.Value.ToString().Trim();
				ItemCode = oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim();
				DrawNo = oForm.Items.Item("DrawNo").Specific.Value.ToString().Trim();

				ProgressBar01.Text = "조회시작!";

				sQry = " EXEC [PS_QM430_01] '";
				sQry += ChkDateF + "','";
				sQry += ChkDateT + "','";
				sQry += CardCode + "','";
				sQry += ItemCode + "','";
				sQry += DrawNo + "'";
				oRecordSet.DoQuery(sQry);

				oMat.Clear();
				oDS_PS_QM430L.Clear();
				oMat.FlushToDataSource();
				oMat.LoadFromDataSource();

				if (oRecordSet.RecordCount == 0)
				{
					oForm.Mode = SAPbouiCOM.BoFormMode.fm_ADD_MODE;
					PS_QM430_Add_MatrixRow01(0, true);
					errMessage = "조회 결과가 없습니다. 확인하세요.";
					throw new Exception();
				}

				for (i = 0; i <= oRecordSet.RecordCount - 1; i++)
				{
					if (i + 1 > oDS_PS_QM430L.Size)
					{
						oDS_PS_QM430L.InsertRecord(i);
					}

					oMat.AddRow();
					oDS_PS_QM430L.Offset = i;

					oDS_PS_QM430L.SetValue("U_LineNum", i, Convert.ToString(i + 1));
					oDS_PS_QM430L.SetValue("U_ColReg01", i, oRecordSet.Fields.Item("Check").Value.ToString().Trim());    //선택
					oDS_PS_QM430L.SetValue("U_ColReg02", i, oRecordSet.Fields.Item("QM410No").Value.ToString().Trim());  //품질검사번호
					oDS_PS_QM430L.SetValue("U_ColReg03", i, oRecordSet.Fields.Item("CardCode").Value.ToString().Trim()); //납품처코드
					oDS_PS_QM430L.SetValue("U_ColReg04", i, oRecordSet.Fields.Item("CardName").Value.ToString().Trim()); //납품처명
					oDS_PS_QM430L.SetValue("U_ColReg05", i, oRecordSet.Fields.Item("ItemCode").Value.ToString().Trim()); //작번
					oDS_PS_QM430L.SetValue("U_ColReg06", i, oRecordSet.Fields.Item("ItemName").Value.ToString().Trim()); //품명
					oDS_PS_QM430L.SetValue("U_ColDt01", i, Convert.ToDateTime(oRecordSet.Fields.Item("ChkDate").Value.ToString().Trim()).ToString("yyyyMMdd")); //검사일자
					oDS_PS_QM430L.SetValue("U_ColReg08", i, oRecordSet.Fields.Item("DrawNo").Value.ToString().Trim());   //도면번호
					oRecordSet.MoveNext();

					ProgressBar01.Value += 1;
					ProgressBar01.Text = ProgressBar01.Value + "/" + oRecordSet.RecordCount + "건 조회중...!";
				}

				oMat.LoadFromDataSource();
				oMat.AutoResizeColumns();
			}
			catch (Exception ex)
			{
				if (ProgressBar01 != null)
				{
					ProgressBar01.Stop();
				}
				if (errMessage != string.Empty)
				{
					PSH_Globals.SBO_Application.MessageBox(errMessage);
				}
				else
				{
					PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
				}
			}
			finally
			{
				ProgressBar01.Stop();
				System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgressBar01);
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
				oForm.Freeze(false);
			}
		}

		/// <summary>
		/// PS_QM430_FlushToItemValue
		/// </summary>
		/// <param name="oUID"></param>
		private void PS_QM430_FlushToItemValue(string oUID)
		{
			string sQry;
			SAPbobsCOM.Recordset oRecordSet = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);

			try
			{
				switch (oUID)
				{
					case "CardCode":
						sQry = " SELECT       CardName";
						sQry += " FROM        [OCRD]";
						sQry += " WHERE       CardCode = '" + oForm.Items.Item("CardCode").Specific.Value.ToString().Trim() + "'";
						oRecordSet.DoQuery(sQry);
						oForm.Items.Item("CardName").Specific.Value = oRecordSet.Fields.Item(0).Value.ToString().Trim();
						break;

					case "ItemCode":
						sQry = " SELECT       FrgnName";
						sQry += " FROM        OITM";
						sQry += " WHERE       ItemCode = '" + oForm.Items.Item("ItemCode").Specific.Value.ToString().Trim() + "'";
						oRecordSet.DoQuery(sQry);
						oForm.Items.Item("ItemName").Specific.Value = oRecordSet.Fields.Item(0).Value.ToString().Trim();
						break;
				}
			}
			catch (Exception ex)
			{
				PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + ex.Message);
			}
			finally
			{
				System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet);
			}
		}

