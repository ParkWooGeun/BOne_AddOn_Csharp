        /// <summary>
        /// 데이터 조회
        /// </summary>
        private void PS_SD085_LoadData()
        {
            int i;
            string sQry;
            string DocDateTo;
            string BPLId;
            string DocDateFr;
            string CardCode;
            string errMessage = string.Empty;
            SAPbouiCOM.ProgressBar ProgBar01 = null;
            SAPbobsCOM.Recordset oRecordSet01 = PSH_Globals.oCompany.GetBusinessObject(SAPbobsCOM.BoObjectTypes.BoRecordset);
            
            try
            {
                oForm.Freeze(true);
                ProgBar01 = PSH_Globals.SBO_Application.StatusBar.CreateProgressBar("", 0, false);

                oMat01.Clear();
                oDS_PS_USERDS01.Clear();

                BPLId = oForm.Items.Item("BPLId").Specific.Value.ToString().Trim();
                DocDateFr = oForm.Items.Item("DocDateFr").Specific.Value.ToString().Trim();
                DocDateTo = oForm.Items.Item("DocDateTo").Specific.Value.ToString().Trim();
                CardCode = oForm.Items.Item("CardCode").Specific.Value.ToString().Trim();

                if (string.IsNullOrEmpty(BPLId))
                {
                    BPLId = "%";
                }

                if (string.IsNullOrEmpty(DocDateFr))
                {
                    DocDateFr = "18990101";
                }

                if (string.IsNullOrEmpty(DocDateTo))
                {
                    DocDateTo = "20991231";
                }

                if (string.IsNullOrEmpty(CardCode))
                {
                    CardCode = "%";
                }

                sQry = "EXEC [PS_SD085_01] '" + BPLId + "', '" + DocDateFr + "', '" + DocDateTo + "', '" + CardCode + "'";
                oRecordSet01.DoQuery(sQry);

                if (oRecordSet01.RecordCount == 0)
                {
                    errMessage = "조회 결과가 없습니다. 확인하세요.";
                    throw new Exception();
                }

                for (i = 0; i <= oRecordSet01.RecordCount - 1; i++)
                {
                    if (i + 1 > oDS_PS_USERDS01.Size)
                    {
                        oDS_PS_USERDS01.InsertRecord(i);
                    }

                    oMat01.AddRow();
                    oDS_PS_USERDS01.Offset = i;
                    oDS_PS_USERDS01.SetValue("U_LineNum", i, Convert.ToString(i + 1));
                    oDS_PS_USERDS01.SetValue("U_ColDt01", i, oRecordSet01.Fields.Item("DocDate").Value.ToString("yyyyMMdd"));
                    oDS_PS_USERDS01.SetValue("U_ColReg01", i, oRecordSet01.Fields.Item("TransId").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg02", i, oRecordSet01.Fields.Item("Ref1").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg03", i, oRecordSet01.Fields.Item("CardCode").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg04", i, oRecordSet01.Fields.Item("CardName").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg05", i, oRecordSet01.Fields.Item("LineMemo").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg06", i, oRecordSet01.Fields.Item("Account").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColSum01", i, oRecordSet01.Fields.Item("Amt").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColSum02", i, oRecordSet01.Fields.Item("RefAmt").Value.ToString().Trim());
                    oDS_PS_USERDS01.SetValue("U_ColReg07", i, oRecordSet01.Fields.Item("RefNum").Value.ToString().Trim());
                    
                    oRecordSet01.MoveNext();
                    ProgBar01.Value += 1;
                    ProgBar01.Text = ProgBar01.Value + "/" + oRecordSet01.RecordCount + "건 조회중...!";
                }
                oMat01.LoadFromDataSource();
                oMat01.AutoResizeColumns();
            }
            catch (Exception ex)
            {
                if (errMessage != string.Empty)
                {
                    PSH_Globals.SBO_Application.MessageBox(errMessage);
                }
                else
                {
                    PSH_Globals.SBO_Application.MessageBox(System.Reflection.MethodBase.GetCurrentMethod().Name + "_Error : " + (char)13 + ex.Message);
                }
            }
            finally
            {
                oForm.Freeze(false);
                if (ProgBar01 != null)
                {
                    ProgBar01.Stop();
                    System.Runtime.InteropServices.Marshal.ReleaseComObject(ProgBar01);
                }
                System.Runtime.InteropServices.Marshal.ReleaseComObject(oRecordSet01);
            }
        }

