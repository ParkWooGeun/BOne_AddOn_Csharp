					{
						System.Threading.Thread thread = new System.Threading.Thread(PS_SD320_PrintReport);
						thread.SetApartmentState(System.Threading.ApartmentState.STA);
						thread.Start();
					}
				}
				else if (pVal.BeforeAction == false)
				{
